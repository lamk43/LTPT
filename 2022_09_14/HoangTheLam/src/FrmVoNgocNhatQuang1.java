    private void txtMatKhauKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMatKhauKeyPressed
        if(evt.getKeyCode()==10) {
            try {
                taikhoanbean tk = dv.ktdn(Integer.parseInt(txtTenDangNhap.getText()), txtMatKhau.getText());
                if(tk!=null) {
                    new FrmVoNgocNhatQuang2(tk.getMaGv(), tk.getHoten(), tk.getGioiTinh(), tk.getDiaChi(), tk.getMatKhau(), tk.getMaKhoa()).setVisible(true);

                    this.setVisible(false);
                }

                else JOptionPane.showMessageDialog(rootPane, "Thông tin Đăng nhập sai!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            } catch(Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_txtMatKhauKeyPressed

    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        try {
            taikhoanbean tk = dv.ktdn(Integer.parseInt(txtTenDangNhap.getText()), txtMatKhau.getText());
            if(tk!=null) {
                new FrmVoNgocNhatQuang2(tk.getMaGv(), tk.getHoten(), tk.getGioiTinh(), tk.getDiaChi(), tk.getMatKhau(), tk.getMaKhoa()).setVisible(true);
                this.setVisible(false);
            }

            else JOptionPane.showMessageDialog(rootPane, "Thông tin Đăng nhập sai!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

